    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        // Ambil data dari input
        String nama = txtNama.getText();
        String username = txtUsername.getText();
        String password = new String(txtPassword.getPassword());
        String selectedRole = txtRole.getSelectedItem();

        // Validasi input
        if (nama.isEmpty() || username.isEmpty() || password.isEmpty() || selectedRole.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        // Dapatkan id_role yang terkait dengan role yang dipilih
        Integer roleId = getRoleId(selectedRole);

        // Query untuk menyimpan data pengguna baru
        String query = "INSERT INTO tb_user (nama, username, password, id_role, created_at, updated_at) VALUES (?, ?, ?, ?, NOW(), NOW())";

        try (Connection conn = MYSQLConnection.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(query)) {

            pstmt.setString(1, nama);
            pstmt.setString(2, username);
            pstmt.setString(3, password);
            pstmt.setInt(4, roleId);

            int rowsAffected = pstmt.executeUpdate();
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Pengguna berhasil ditambahkan!");
                getDataTable(); // Refresh tabel setelah menyimpan data baru
                clearForm(); // Bersihkan form input setelah menyimpan data
            } else {
                JOptionPane.showMessageDialog(this, "Gagal menambahkan pengguna.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSubmitActionPerformed

    // Method untuk mendapatkan id_role berdasarkan role yang dipilih
    private Integer getRoleId(String role) {
        return roleMap.get(role);
    }

    // Method untuk membersihkan form input setelah menyimpan data
    private void clearForm() {
        txtNama.setText("");
        txtUsername.setText("");
        txtPassword.setText("");
        txtRole.select(0); // Reset pilihan role
    }

