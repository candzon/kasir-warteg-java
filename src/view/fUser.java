    private void getDataTable() {
        model = (DefaultTableModel) tableUser.getModel();
        model.setRowCount(0);

        String query = "SELECT u.*, r.role "
             + "FROM tb_user u "
             + "JOIN tb_role r ON u.id_role = r.id_role "
             + "ORDER BY u.id_user DESC";

        try (Connection conn = MYSQLConnection.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(query);
                ResultSet rs = pstmt.executeQuery()) {

            while (rs.next()) {
                Object[] row = new Object[6];
                row[0] = rs.getString("role");
                row[1] = rs.getString("nama");
                row[2] = rs.getString("username");
                row[3] = rs.getTimestamp("created_at");
                row[4] = rs.getTimestamp("updated_at");
                model.addRow(row);
            }
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
        }
    }

    private void fetchRoles() {
        String query = "SELECT id_role, role FROM tb_role";

        try (Connection conn = MYSQLConnection.getConnection();
                PreparedStatement pstmt = conn.prepareStatement(query);
                ResultSet rs = pstmt.executeQuery()) {

            while (rs.next()) {
                int idRole = rs.getInt("id_role");
                String role = rs.getString("role");

                // Menambahkan role ke dalam Choice
                txtRole.add(role);
                // Opsional: Simpan id_role sebagai nilai terkait dengan role di Choice
                // Di sini Anda dapat menggunakan HashMap atau lainnya untuk memetakan id_role ke role
                // atau menggunakan cara yang sesuai dengan kebutuhan aplikasi Anda.
                // Simpan id_role sebagai nilai terkait dengan role di HashMap
                roleMap.put(role, idRole);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

